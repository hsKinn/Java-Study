package CompoundOperator;

public class CompoundOperator {
    
    public static void main(String[] args) {
        
        int num1 = 5;
        int num2 = 4;
 
        num1 += num2; // num1 = num1 + num2
        System.out.println("num1의 값: " + num1);
 
        num2 *= num1; // num2 = num2 * num1
        System.out.println("num2의 값: " + num2);
    }
}
