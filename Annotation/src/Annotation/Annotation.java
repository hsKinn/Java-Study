package Annotation;

public class Annotation {

	public static void main(String[] args) {
		
		int num1; // 정수형 num1 선언
		int num2; // 정수형 num2 선언
		int result; // 정수형 result 선언
		
		num1 = 100; // num1에 100 값 대입
		num2 = 200; // num2에 200 값 대입
		result = num1 + num2; // result에 num1 + num2 값 대입
		
		System.out.println(result); // result 출력
		
		
		/*
		 * 정수형 num1, num2를 선언하여 값을 대입 후 result에 더한 결과를 넣고 출력한다 
		 */
		int num11;
		int num22;
		int result2;
		
		num1 = 100;
		num2 = 200;
		result = num1 + num2;
		
		System.out.println(result);
	}
}
